                <h1>Yoga sur le Toil</h1>
                <h5>Un projet photographique hors du commun !</h5>
                <p>C’est un hommage à mes élèves et à tous les yogis des temps modernes,
                    une réconciliation entre la jungle urbaine et l’individu en quête de spiritualité,
                    un message d’espoir à tous les citadins du monde.
                </p>

                <p>
                    Depuis novembre 2016, nous nous réunissons une fois par mois sur différents toits de Casablanca,
                    le temps d’une séance de yoga, d’une vraie parenthèse dans notre quotidien. Le jeune et talentueux
                    artiste/photographe, Ali Berrada, nous suit depuis le début de l’aventure...
                    </p>
                <p>
                    Le fruit de notre travail, sera prochainement partagé avec le grand public, lors d’une exposition
                    itinérante.

                </p>
                <h2>Naissance d’un projet </h2>

                <p>
                    L’idée de ce projet est née tout simplement de ma pratique personnelle de yoga dont j’ai pu expérimenter
                    les bienfaits en pratiquant sur le toit de mon appartement, en plein Casablanca.
                    </p>
                <p>
                    Je ne m’y attendais pas du tout, mais, étrangement, au milieu du linge étendu à sécher, des câbles
                    emmêlés et des paraboles rouillées, je trouvais le calme et le détachement nécessaire pour tirer le
                    maximum de bénéfices de ma pratique.
                   </p>
                <p>
                    C’est comme si, par magie, une petite fenêtre s’ouvrait au cœur de l’effervescence urbaine, et il
                    devenait alors possible de voler au temps quelques instants d’immensité. Je me reliais à moi-même et à l’absolu.
                   </p>
                <p>
                    Pratiquer le yoga sur le toit a complètement transformé la façon dont je perçois mon quotidien
                    à Casablanca, d’où l’envie de partager cette expérience avec mes élèves et, pourquoi pas, la sublimer
                    à travers un projet artistique.

                </p>

                <h2>L’objectif </h2>
                <p>
                    Le projet s’inscrit, avant tout, dans une démarche pédagogique. Sa finalité est d’encourager la
                    jeune génération de yogis casablancais à explorer de nouveaux espaces le temps d'une parenthèse, à
                    suspendre l'agitation extérieure pour se concentrer sur l'essentiel.
                    </p>
                <p>
                    La participation des élèves au projet s’est faite sur le mode du volontariat, sans aucun critère de
                    sélection. Le yoga sur le toit est accessible à tous ceux qui sont portés par leur motivation et leur envie de partage.
                </p>
                <p>
                    L’objectif artistique consiste, quant à lui, à capturer et transmettre à travers la photo, la
                    concentration, la force, la grâce et la sérénité de ces yoginis et yogis des temps modernes.
                   </p>
                <p>
                    De mettre en avant le contraste apparent, mais non impossible, entre la jungle urbaine, agressive
                    et matérialiste, et la quête spirituelle de l’individu.
                </p>
                <p>
                    C’est également un hommage rendu aux femmes et aux hommes de Casablanca, dans
                    leur recherche d’un meilleur équilibre dans leur vie de citadins.

                </p>
