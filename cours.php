                <h2>YogaDooda</h2>
                <h5>Le yoga pour enfants</h5>
                <p>
                    Réduction du niveau de stress et de la douleur, amélioration de la circulation et de la digestion,
                    meilleure alignement du corps, augmentation de la concentration, une respiration plus profonde et
                    plus détendue, développement d’un sentiment de bien-être et de paix intérieure……les innombrables
                    bienfaits du Yoga sont de nos jours reconnus par la science.
                </p>
                <blockquote>
                    <p>
                        Pourtant, la première question que tous les parents se posent est :<br>
                        Ces bienfaits s’appliquent-ils aussi aux enfants ?<br>
                        La réponse est OUI, et bien plus encore !

                    </p>
                </blockquote>
                <p>
                    De nos jours, les enfants sont très vite exposés aux pressions de réussite sociales et académiques,
                    et cela même dans les activités sportives qui sont souvent et malheureusement basées sur la performance et la compétitivité.
                    C’est pourtant durant l’enfance que se développe l’estime de soi et que se construit l’image positive
                    qui les accompagnera dans leur vie d’adulte.

                </p><img class="alignleft" src="images/content/postimg01.jpg" alt="">
                <p>
                    Le yoga pour enfants, à travers son approche divertissante et non-compétitive, permet aux enfants de
                    développer non seulement leur force physique et mentale, leur coordination, leur souplesse et leur équilibre,
                    mais également leur conscience du corps, la concentration et la confiance en soi.
                </p>
                <p>
                    Les séances de YOGAdooda sont construites autour de thèmes de voyages, de jeux, de chansons, à la
                    rencontre de nombreux animaux et espèces vivantes et divers peuples du monde entier. Ces périples
                    imaginaires sont d’excellents moyens pour leur en apprendre d’avantage sur les animaux et leurs habitats,
                    la diversité des cultures, tout en les sensibilisant au respect de l’environnement.

                </p><img class="alignleft" src="images/content/postimg01.jpg" alt="">
                <p>
                    En plus des postures et des exercices de respiration yoguiques, c’est au final toute la culture et
                    les valeurs du Yoga qui sont transmises dans ce cours. (tolérance, non-jugement, pensée positive,
                    respect de soi et des autres …)
                </p>
