                <h1>Cours DarYoga</h1>
                <p>Recently I was out to dinner with a <a href="#">big group of colleagues</a>, chatting while we waited to be seated in a restaurant. I didn’t notice the sudden lull that had come over the group until someone commented, “So we’re all doing this, huh?”</p>

                <h2>Séance d’essai </h2>

                <p>
                    Vous êtes nouveau ?
                </p>
                <p>
                    Venez essayer un cours gratuitement. Cela vous permettra de prendre connaissance des lieux, du style
                    de yoga enseigné et bien sûr de rencontrer le professeur.
                </p>
                <p>
                    Il est préférable de nous contacter préalablement par email pour programmer votre séance.
                </p>

                <h2>Session-Débutants </h2>
                <p>
                    La Session-Débutants est incontournable lorsque vous démarrer le yoga. Elle s’étale sur 3 mois, à
                    raison d’une séance de 1h30 par semaine, et s’adressent spécifiquement aux novices.
                    A travers une pédagogie alliant théorie et pratique, les élèves découvrent le yoga pas à pas et dans
                    son intégralité. L’accent est tout de même mis sur l’apprentissage technique des postures et de la
                    respiration. L’explication des bienfaits physique et psychique des postures et des exercices de
                    respiration permettent de donner du sens et de mieux comprendre la force du yoga.
                </p>
                <p>
                    La session-débutants couvre aussi d’autres aspects du yoga, telle que la relaxation, l’alimentation
                    yoguique et la méditation. Les élèves bénéficient de plus d’attention et de corrections de la part
                    du professeur, et ont ainsi le temps de poser toutes leurs questions.
                </p>

                <h2>Séance Intermédiaire</h2>
                <p>
                    Données plusieurs fois par semaine, les séances intermédiaires dites « type » regroupent tous les élèves
                    ayant effectué la session-débutants, indépendamment de leur degré de souplesse et de leur forme physique.
                </p>
                <p>
                    Ce sont des séances où chacun est amené à travailler sur son souffle, son corps, et sur sa
                    concentration de manière bienveillante, sans jugement et sans compétition. Elles donnent l’opportunité
                    aux élèves de pratiquer de façon régulière, en profondeur, leur permettant ainsi de progresser à leur rythme.
                </p>

                <h2>Séance Avancée</h2>
                <p>
                    Elles s’adressent aux élèves qui ont une bonne maitrise des postures de base, et en particulier
                    de la posture sur la tête (Sirsasan).
                </p>
                <p>
                    Le rythme y est plus dynamique et la durée de tenue des postures plus longue. Le cours inclus
                    également de nouvelles postures et variantes, telle que le scorpion (Vrichicasana).
                </p>
                <h2>Séance en plein Air</h2>
                <p>
                    Lorsque les températures le permettent, les cours de yoga sont donnés en plein air, dans le jardin
                    même de l’atelier, en plein cœur de Casa.
                </p>
                <p>
                    A ciel ouvert, entouré de verdure, les bienfaits du yoga sont amplifiés. Le corps et le cerveau
                    sont mieux oxygénés et il devient alors plus facile d’apaiser le ruminement du mental.
                </p>
                <p>
                    Le contact avec la végétation favorise la relaxation et permet de se recharger en énergie.
                    Les séances en plein air offre une réelle bouffée d’oxygène à nos vies.
                </p>
